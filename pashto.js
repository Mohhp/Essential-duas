// LANGUAGE TOGGLE & APPLY FUNCTIONS
// ============================================

function getCurrentLang() {
    return localStorage.getItem('crown_lang') || 'en';
}

function toggleLanguage() {
    const lang = getCurrentLang() === 'en' ? 'ps' : 'en';
    localStorage.setItem('crown_lang', lang);
    applyLanguage(lang);
    // Re-run updateStats so stat labels reflect the new language
    if (typeof updateStats === 'function') updateStats();
}

function applyLanguage(lang) {
    if (!lang) lang = getCurrentLang();
    const isPS = lang === 'ps';

    // Body class
    document.body.classList.toggle('lang-ps', isPS);
    document.documentElement.setAttribute('lang', isPS ? 'ps' : 'en');

    // Toggle button highlight
    const enBtn = document.getElementById('langEN');
    const psBtn = document.getElementById('langPS');
    if (enBtn) enBtn.classList.toggle('active-lang', !isPS);
    if (psBtn) psBtn.classList.toggle('active-lang', isPS);

    if (!isPS) {
        // Restore English — remove injected Pashto elements, restore original text
        restoreEnglish();
        return;
    }

    // === PASHTO MODE ===

    // Hero section
    const h1 = document.querySelector('.gold-text');
    if (h1) { if (!h1.getAttribute('data-en')) h1.setAttribute('data-en', h1.textContent); h1.textContent = PS_UI.heroTitle; }
    const sub = document.querySelector('.subtitle');
    if (sub) { if (!sub.getAttribute('data-en')) sub.setAttribute('data-en', sub.textContent); sub.textContent = PS_UI.heroSubtitle; }
    const sadaqa = document.querySelector('.hero [style*="font-style:italic"]');
    if (sadaqa) { if (!sadaqa.getAttribute('data-en')) sadaqa.setAttribute('data-en', sadaqa.textContent); sadaqa.textContent = PS_UI.sadaqaJariya; }
    const intro = document.querySelector('.intro-text');
    if (intro) { if (!intro.getAttribute('data-en')) intro.setAttribute('data-en', intro.textContent); intro.textContent = PS_UI.introText; }

    // Category grid cards
    document.querySelectorAll('.cat-card').forEach(card => {
        const cat = card.getAttribute('data-cat');
        if (!cat) return;
        const nameEl = card.querySelector('.cat-card-name');
        const countEl = card.querySelector('.cat-card-count');
        if (nameEl) {
            if (!nameEl.getAttribute('data-en')) nameEl.setAttribute('data-en', nameEl.textContent);
            if (PS_UI.catCardTitles[cat]) nameEl.textContent = PS_UI.catCardTitles[cat];
        }
        if (countEl) {
            if (!countEl.getAttribute('data-en')) countEl.setAttribute('data-en', countEl.textContent);
            const num = countEl.textContent.replace(/[^0-9]/g, '');
            countEl.textContent = num + ' ' + PS_UI.duasSuffix;
        }
    });

    // Back to categories button
    const backBtn = document.getElementById('backToGrid');
    if (backBtn) {
        if (!backBtn.getAttribute('data-en')) backBtn.setAttribute('data-en', backBtn.textContent);
        backBtn.textContent = PS_UI.backToCategories;
    }

    // Category detail header (dynamic — update if currently visible)
    const cdhTitle = document.getElementById('cdhTitle');
    const cdhSubtitle = document.getElementById('cdhSubtitle');
    if (cdhTitle && cdhTitle.textContent) {
        // Find the current category from localStorage or active state
        const activeCat = localStorage.getItem('crown_active_category');
        if (activeCat && PS_UI.catCardTitles[activeCat]) {
            cdhTitle.textContent = PS_UI.catCardTitles[activeCat];
        }
        if (activeCat && PS_UI.catCardSubtitles[activeCat]) {
            cdhSubtitle.textContent = PS_UI.catCardSubtitles[activeCat];
        }
    }

    // Search placeholder
    const si = document.getElementById('searchInput');
    if (si) { if (!si.getAttribute('data-en-ph')) si.setAttribute('data-en-ph', si.placeholder); si.placeholder = PS_UI.searchPlaceholder; }

    // Toolbar buttons
    document.querySelectorAll('.toolbar-btn').forEach(btn => {
        if (!btn.getAttribute('data-en')) btn.setAttribute('data-en', btn.textContent);
        const t = btn.textContent.trim();
        if (t.includes('Expand All')) btn.textContent = PS_UI.expandAll;
        else if (t.includes('Collapse All')) btn.textContent = PS_UI.collapseAll;
        else if (t.includes('Tasbeeh')) btn.textContent = PS_UI.tasbeeh;
        else if (t.includes('Random')) btn.textContent = PS_UI.randomDua;
        else if (t.includes('Memorize')) btn.textContent = PS_UI.memorize;
    });

    // Category pills
    document.querySelectorAll('.pill').forEach(pill => {
        const cat = pill.getAttribute('data-category');
        if (!cat) return;
        const countEl = pill.querySelector('.count');
        const countHTML = countEl ? countEl.outerHTML : '';
        if (!pill.getAttribute('data-en')) pill.setAttribute('data-en', pill.innerHTML);
        if (PS_UI.cats[cat]) pill.innerHTML = PS_UI.cats[cat] + ' ' + countHTML;
    });

    // No results
    const nrText = document.querySelector('.no-results-text');
    if (nrText) { if (!nrText.getAttribute('data-en')) nrText.setAttribute('data-en', nrText.textContent); nrText.textContent = PS_UI.noResults; }

    // Section headers
    document.querySelectorAll('.section-header').forEach(sh => {
        const sec = sh.getAttribute('data-section');
        if (!sec || !PS_UI.sections[sec]) return;
        const title = sh.querySelector('.section-title');
        const subtitle = sh.querySelector('.section-subtitle');
        const hint = sh.querySelector('.section-collapse-hint');
        if (title) { if (!title.getAttribute('data-en')) title.setAttribute('data-en', title.textContent); title.textContent = PS_UI.sections[sec].title; }
        if (subtitle) { if (!subtitle.getAttribute('data-en')) subtitle.setAttribute('data-en', subtitle.textContent); subtitle.textContent = PS_UI.sections[sec].subtitle; }
        if (hint) {
            if (!hint.getAttribute('data-en')) hint.setAttribute('data-en', hint.textContent);
            hint.textContent = sh.classList.contains('collapsed') ? PS_UI.tapExpand : PS_UI.tapCollapse;
        }
    });

    // Dua cards — titles, category tags, action buttons, injecting Pashto translation/virtue
    document.querySelectorAll('.dua-card').forEach(card => {
        const id = parseInt(card.getAttribute('data-id'));

        // Title
        const titleEl = card.querySelector('.dua-title');
        if (titleEl && PS_UI.duaTitles[id]) {
            if (!titleEl.getAttribute('data-en')) titleEl.setAttribute('data-en', titleEl.innerHTML);
            const badge = titleEl.querySelector('.auth-badge');
            let badgeHTML = '';
            if (badge) {
                const origText = badge.textContent.trim();
                const psText = PS_UI.badges[origText] || origText;
                badgeHTML = ' <span class="' + badge.className + '">' + psText + '</span>';
            }
            titleEl.innerHTML = PS_UI.duaTitles[id] + badgeHTML;
        }

        // Category tag
        const catTag = card.querySelector('.dua-category-tag');
        if (catTag && PS_UI.duaCatTags[id]) {
            if (!catTag.getAttribute('data-en')) catTag.setAttribute('data-en', catTag.textContent);
            catTag.textContent = PS_UI.duaCatTags[id];
        }

        // Inject translation-ps and virtue-tag-ps if not already there
        if (PS_DUAS[id]) {
            const inner = card.querySelector('.card-body-inner');
            if (inner && !inner.querySelector('.translation-ps')) {
                const refEl = inner.querySelector('.reference');
                // Insert Pashto translation after English translation
                const tDiv = document.createElement('div');
                tDiv.className = 'translation-ps';
                tDiv.setAttribute('dir', 'rtl');
                tDiv.textContent = PS_DUAS[id].t;
                const enTrans = inner.querySelector('.translation');
                if (enTrans && enTrans.nextSibling) {
                    inner.insertBefore(tDiv, enTrans.nextSibling);
                } else if (refEl) {
                    inner.insertBefore(tDiv, refEl);
                }
            }
            if (PS_DUAS[id].v && !inner.querySelector('.virtue-tag-ps')) {
                const vDiv = document.createElement('div');
                vDiv.className = 'virtue-tag-ps';
                vDiv.setAttribute('dir', 'rtl');
                vDiv.innerHTML = '<span class="gem">💎</span> ' + PS_DUAS[id].v;
                const virtueEn = inner.querySelector('.virtue-tag');
                if (virtueEn && virtueEn.nextSibling) {
                    inner.insertBefore(vDiv, virtueEn.nextSibling);
                } else {
                    const copyRow = inner.querySelector('.copy-row');
                    if (copyRow) inner.insertBefore(vDiv, copyRow);
                }
            }
        }

        // Action buttons text
        card.querySelectorAll('.action-btn').forEach(btn => {
            if (!btn.getAttribute('data-en')) btn.setAttribute('data-en', btn.innerHTML);
            const t = btn.textContent.trim();
            if (t.includes('Copy Arabic')) btn.innerHTML = PS_UI.copyArabic;
            else if (t === '↗ Share') btn.innerHTML = PS_UI.share;
            else if (t === '✓ Mark Read') btn.innerHTML = PS_UI.markRead;
            else if (t === '✓ Read') btn.innerHTML = PS_UI.alreadyRead;
            else if (t === '🔊 Listen') btn.innerHTML = PS_UI.listen;
            else if (t === '⏹ Stop') btn.innerHTML = PS_UI.stop;
            else if (t.includes('Share Image')) btn.innerHTML = PS_UI.shareImage;
        });
    });

    // Bottom nav
    document.querySelectorAll('.bottom-nav-item').forEach(item => {
        const nav = item.getAttribute('data-nav');
        const label = item.querySelector('span:not(.icon)');
        if (!label) return;
        if (!label.getAttribute('data-en')) label.setAttribute('data-en', label.textContent);
        if (nav === 'home') label.textContent = PS_UI.home;
        else if (nav === 'routine') label.textContent = PS_UI.routine;
        else if (nav === 'tasbeeh') label.textContent = PS_UI.tasbeehNav;
        else if (nav === 'saved') label.textContent = PS_UI.saved;
        else if (nav === 'prayer') label.textContent = PS_UI.prayerNav;
    });

    // Tasbeeh panel
    const tasbeehTitle = document.querySelector('.tasbeeh-title');
    if (tasbeehTitle) { if (!tasbeehTitle.getAttribute('data-en')) tasbeehTitle.setAttribute('data-en', tasbeehTitle.textContent); tasbeehTitle.textContent = PS_UI.digitalTasbeeh; }
    const tapBtn = document.querySelector('.tasbeeh-tap-btn');
    if (tapBtn) { if (!tapBtn.getAttribute('data-en')) tapBtn.setAttribute('data-en', tapBtn.textContent); tapBtn.textContent = PS_UI.tap; }
    const resetBtn = document.querySelector('.tasbeeh-ctrl-btn');
    if (resetBtn) { if (!resetBtn.getAttribute('data-en')) resetBtn.setAttribute('data-en', resetBtn.textContent); resetBtn.textContent = PS_UI.reset; }
    const totalLabel = document.querySelector('.tasbeeh-lifetime');
    if (totalLabel) {
        if (!totalLabel.getAttribute('data-en')) totalLabel.setAttribute('data-en', totalLabel.innerHTML);
        const span = totalLabel.querySelector('span');
        totalLabel.innerHTML = PS_UI.total + ': <span>' + (span ? span.textContent : '0') + '</span>';
    }
    const targetLabel = document.getElementById('tasbeehTargetLabel');
    if (targetLabel) {
        if (!targetLabel.getAttribute('data-en')) targetLabel.setAttribute('data-en', targetLabel.textContent);
        const t = targetLabel.textContent;
        if (t.includes('OPEN COUNT')) targetLabel.textContent = PS_UI.openCount;
        else targetLabel.textContent = PS_UI.target + ': ' + t.replace(/[^0-9]/g, '');
    }

    // Footer
    const footerText = document.querySelector('.footer-text');
    if (footerText) {
        if (!footerText.getAttribute('data-en')) footerText.setAttribute('data-en', footerText.innerHTML);
        footerText.innerHTML = PS_UI.footerText + '<br>' + PS_UI.footerSub;
    }

    // Prayer panel
    const prayerTitle = document.querySelector('.prayer-panel-content h2');
    if (prayerTitle) { if (!prayerTitle.getAttribute('data-en')) prayerTitle.setAttribute('data-en', prayerTitle.textContent); prayerTitle.textContent = PS_UI.prayerTimesTitle; }
    const prayerLocText = document.getElementById('prayerLocText');
    if (prayerLocText) {
        const t = prayerLocText.textContent;
        if (t === 'Tap to detect location') prayerLocText.textContent = PS_UI.tapToDetect;
        else if (t === 'Detecting location...') prayerLocText.textContent = PS_UI.detectingLocation;
        else if (t.includes('denied')) prayerLocText.textContent = PS_UI.locationDenied;
    }
    const countdownLabel = document.querySelector('.prayer-countdown-label');
    if (countdownLabel) { if (!countdownLabel.getAttribute('data-en')) countdownLabel.setAttribute('data-en', countdownLabel.textContent); countdownLabel.textContent = PS_UI.nextPrayer; }
    // Translate prayer names in grid
    document.querySelectorAll('.prayer-time-name').forEach(el => {
        if (!el.getAttribute('data-en')) el.setAttribute('data-en', el.textContent);
        const key = el.textContent.trim().toLowerCase();
        if (PS_UI.prayerNames[key]) el.textContent = PS_UI.prayerNames[key];
    });
    // Prayer badges
    document.querySelectorAll('.prayer-now-badge').forEach(el => el.textContent = PS_UI.now);
    document.querySelectorAll('.prayer-next-badge').forEach(el => el.textContent = PS_UI.next);
    // Qibla
    const qiblaTitle = document.querySelector('.qibla-title');
    if (qiblaTitle) { if (!qiblaTitle.getAttribute('data-en')) qiblaTitle.setAttribute('data-en', qiblaTitle.textContent); qiblaTitle.textContent = PS_UI.qiblaTitle; }
    const qiblaDeg = document.getElementById('qiblaDegree');
    if (qiblaDeg && qiblaDeg.textContent.includes('from North')) {
        qiblaDeg.textContent = qiblaDeg.textContent.replace('from North', PS_UI.qiblaFromNorth);
    }
    const qiblaStatus = document.getElementById('qiblaStatus');
    if (qiblaStatus) {
        if (qiblaStatus.textContent === 'Enable location to see Qibla') qiblaStatus.textContent = PS_UI.enableQibla;
        else if (qiblaStatus.textContent.includes('Point your phone')) qiblaStatus.textContent = PS_UI.qiblaPointPhone;
    }
    // Notification toggle label
    const notifyLabel = document.querySelector('.prayer-notify-label');
    if (notifyLabel) { if (!notifyLabel.getAttribute('data-en')) notifyLabel.setAttribute('data-en', notifyLabel.textContent); notifyLabel.textContent = PS_UI.prayerAlerts; }

    // Progress share button
    const progressShareBtn = document.querySelector('.progress-share-btn');
    if (progressShareBtn) { if (!progressShareBtn.getAttribute('data-en')) progressShareBtn.setAttribute('data-en', progressShareBtn.textContent); progressShareBtn.textContent = PS_UI.shareProgress; }

    // SR badges
    document.querySelectorAll('.sr-review-badge').forEach(el => el.textContent = PS_UI.srReviewDue);
    document.querySelectorAll('.sr-rating-btn.sr-easy').forEach(el => el.textContent = PS_UI.srEasy);
    document.querySelectorAll('.sr-rating-btn.sr-hard').forEach(el => el.textContent = PS_UI.srHard);
    // Memorize button with due count
    const memBtn = document.getElementById('memorizeToggle');
    if (memBtn) {
        if (!memBtn.getAttribute('data-en')) memBtn.setAttribute('data-en', memBtn.textContent);
        const t = memBtn.textContent;
        const match = t.match(/\((\d+) due\)/);
        if (match) memBtn.textContent = PS_UI.memorize + ' (' + match[1] + ' ' + PS_UI.srDue + ')';
        else memBtn.textContent = PS_UI.memorize;
    }
}

function restoreEnglish() {
    // Restore all elements that have data-en
    document.querySelectorAll('[data-en]').forEach(el => {
        el.innerHTML = el.getAttribute('data-en');
    });
    // Restore search placeholder
    const si = document.getElementById('searchInput');
    if (si && si.getAttribute('data-en-ph')) si.placeholder = si.getAttribute('data-en-ph');
    // Section collapse hints
    document.querySelectorAll('.section-collapse-hint').forEach(hint => {
        const sh = hint.closest('.section-header');
        if (sh) hint.textContent = sh.classList.contains('collapsed') ? 'tap to expand' : 'tap to collapse';
    });
    // Restore category detail header to English if a category is open
    const activeCat = localStorage.getItem('crown_active_category');
    if (activeCat && typeof CATEGORY_META !== 'undefined' && CATEGORY_META[activeCat]) {
        const meta = CATEGORY_META[activeCat];
        const cdhTitle = document.getElementById('cdhTitle');
        const cdhSubtitle = document.getElementById('cdhSubtitle');
        if (cdhTitle) cdhTitle.textContent = meta.title;
        if (cdhSubtitle) cdhSubtitle.textContent = meta.subtitle;
    }
}

// Patch showToast for Pashto — deferred until showToast exists
let _toastPatched = false;
function patchToastForPashto() {
    if (_toastPatched || typeof window.showToast !== 'function') return;
    const _orig = window.showToast;
    window.showToast = function(msg) {
        if (getCurrentLang() === 'ps' && PS_UI.toasts[msg]) {
            _orig(PS_UI.toasts[msg]);
        } else {
            _orig(msg);
        }
    };
    _toastPatched = true;
}

// Auto-patch after DOM is ready (main script will have loaded)
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', patchToastForPashto);
} else {
    setTimeout(patchToastForPashto, 0);
}
